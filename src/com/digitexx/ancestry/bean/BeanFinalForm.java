package com.digitexx.ancestry.bean;

import java.io.Serializable;

/**
 * @author lqnhu
 * 
 */
@SuppressWarnings("serial")
public class BeanFinalForm implements Serializable {
	private int id = 0;
	private String form_name = "";
	private String form_ids = "";
	private String form_rules = "";

	/**
	 * @return the id
	 */
	public int getId() {
		return id;
	}

	/**
	 * @return the form_name
	 */
	public String getForm_name() {
		return form_name;
	}

	/**
	 * @return the form_ids
	 */
	public String getForm_ids() {
		return form_ids;
	}

	/**
	 * @return the form_rules
	 */
	public String getForm_rules() {
		return form_rules;
	}

	/**
	 * @param id
	 *            the id to set
	 */
	public void setId(int id) {
		this.id = id;
	}

	/**
	 * @param form_name
	 *            the form_name to set
	 */
	public void setForm_name(String form_name) {
		this.form_name = form_name;
	}

	/**
	 * @param form_ids
	 *            the form_ids to set
	 */
	public void setForm_ids(String form_ids) {
		this.form_ids = form_ids;
	}

	/**
	 * @param form_rules
	 *            the form_rules to set
	 */
	public void setForm_rules(String form_rules) {
		this.form_rules = form_rules;
	}

	@Override
	public String toString() {
		return form_name;
	}

}
